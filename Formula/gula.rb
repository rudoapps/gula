class Gula < Formula
  desc "Instalador de componentes de gula"
  homepage "https://github.com/rudoapps/gula"
  url "https://github.com/rudoapps/homebrew-gula/archive/refs/tags/0.0.33.tar.gz"
  sha256 "f111b12331b5b5ef4a121f389bf4d64e2da63e4009fb41da661763678db59585"
  license "MIT"

  # Dependencias
  depends_on "ruby"
  depends_on "jq" # AÃ±ade jq como una dependencia

  def install
    bin.install "gula"
    (share/"support/scripts").install "scripts"
  end

  def post_install
    bin.install_symlink share/"support/scripts" => "gula-scripts"
  end

  test do
    system "#{bin}/gula", "install", "prueba"
  end
end
